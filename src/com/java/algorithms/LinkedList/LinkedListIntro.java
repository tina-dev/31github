package com.java.algorithms.LinkedList;

import java.util.LinkedList;

public class LinkedListIntro {
    public static void main(String [] args) {
        LinkedList<Integer> list = new LinkedList<>();
        list.add(1);
        list.add(2);
        list.add(3);
        for( int lists: list) {
            System.out.println(lists);

        }
    }
}
